const asyncHandler = require("../utils/asyncHandler");
const ApiError = require("../utils/ApiError");

// GET /api/addresses
const getAddresses = asyncHandler(async (req, res) => {
  const addresses = req.user.savedAddresses || [];
  res.status(200).json({
    success: true,
    data: addresses
  });
});

// POST /api/addresses
const addAddress = asyncHandler(async (req, res) => {
  const { label, isDefault, addressLine, city, pincode, latitude, longitude } = req.body;
  
  if (!addressLine || !city) {
    throw new ApiError(400, "Address line and city are required.");
  }

  const newAddress = {
    label: label || "Home",
    isDefault: isDefault || false,
    addressLine,
    city,
    pincode,
    location: {
      type: "Point",
      coordinates: [longitude || 0, latitude || 0]
    }
  };

  if (newAddress.isDefault || req.user.savedAddresses.length === 0) {
    newAddress.isDefault = true;
    req.user.savedAddresses.forEach(addr => addr.isDefault = false);
  }

  req.user.savedAddresses.push(newAddress);
  await req.user.save();

  // Return the newly added address (it will have an _id generated by Mongoose)
  const added = req.user.savedAddresses[req.user.savedAddresses.length - 1];

  res.status(201).json({
    success: true,
    message: "Address saved successfully.",
    data: added
  });
});

// PUT /api/addresses/:id
const updateAddress = asyncHandler(async (req, res) => {
  const { id } = req.params;
  const { label, isDefault, addressLine, city, pincode, latitude, longitude } = req.body;

  const address = req.user.savedAddresses.id(id);
  if (!address) {
    throw new ApiError(404, "Address not found.");
  }

  if (label !== undefined) address.label = label;
  if (addressLine !== undefined) address.addressLine = addressLine;
  if (city !== undefined) address.city = city;
  if (pincode !== undefined) address.pincode = pincode;
  
  if (latitude !== undefined && longitude !== undefined) {
    address.location = {
      type: "Point",
      coordinates: [longitude, latitude]
    };
  }

  if (isDefault) {
    req.user.savedAddresses.forEach(addr => addr.isDefault = false);
    address.isDefault = true;
  } else if (isDefault === false) {
    address.isDefault = false;
  }

  await req.user.save();

  res.status(200).json({
    success: true,
    message: "Address updated successfully.",
    data: address
  });
});

// DELETE /api/addresses/:id
const deleteAddress = asyncHandler(async (req, res) => {
  const { id } = req.params;
  
  const addressIndex = req.user.savedAddresses.findIndex(a => a._id.toString() === id);
  if (addressIndex === -1) {
    throw new ApiError(404, "Address not found.");
  }

  const wasDefault = req.user.savedAddresses[addressIndex].isDefault;
  req.user.savedAddresses.splice(addressIndex, 1);

  // If we deleted the default address, make the first one default
  if (wasDefault && req.user.savedAddresses.length > 0) {
    req.user.savedAddresses[0].isDefault = true;
  }

  await req.user.save();

  res.status(200).json({
    success: true,
    message: "Address deleted successfully."
  });
});

module.exports = {
  getAddresses,
  addAddress,
  updateAddress,
  deleteAddress
};
